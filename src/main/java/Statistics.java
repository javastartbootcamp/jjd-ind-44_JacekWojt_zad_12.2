public class Statistics {
    private double avgSalary;
    private double minSalary;
    private double maxSalary;
    private int numberOfItEmployee;
    private int numberOfSupportEmployee;
    private int numberOfManagementEmployee;

    public double getAvgSalary() {
        return avgSalary;
    }

    public void setAvgSalary(double avgSalary) {
        this.avgSalary = avgSalary;
    }

    public double getMinSalary() {
        return minSalary;
    }

    public void setMinSalary(double minSalary) {
        this.minSalary = minSalary;
    }

    public double getMaxSalary() {
        return maxSalary;
    }

    public void setMaxSalary(double maxSalary) {
        this.maxSalary = maxSalary;
    }

    public int getNumberOfItEmployee() {
        return numberOfItEmployee;
    }

    public void setNumberOfItEmployee(int numberOfItEmployee) {
        this.numberOfItEmployee = numberOfItEmployee;
    }

    public int getNumberOfSupportEmployee() {
        return numberOfSupportEmployee;
    }

    public void setNumberOfSupportEmployee(int numberOfSupportEmployee) {
        this.numberOfSupportEmployee = numberOfSupportEmployee;
    }

    public int getNumberOfManagementEmployee() {
        return numberOfManagementEmployee;
    }

    public void setNumberOfManagementEmployee(int numberOfManagementEmployee) {
        this.numberOfManagementEmployee = numberOfManagementEmployee;
    }
}
